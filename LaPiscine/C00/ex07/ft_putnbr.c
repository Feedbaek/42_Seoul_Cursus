#include <unistd.h>

void	ft_putnbr(int nb)
{
	char o;

	if (nb >= 0)
	{
		o = '0' + nb % 10;
		if (nb / 10 != 0)
			ft_putnbr(nb / 10);
		write(1, &o, 1);
	}
	else
	{
		write(1, &"-", 1);
		o = '0' - nb % 10;
		if (-(nb / 10) != 0)
			ft_putnbr(-(nb / 10));
		write(1, &o, 1);
	}
}
